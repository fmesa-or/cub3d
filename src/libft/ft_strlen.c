#include "cub3d.h"

/********************************************
 * Returns the number of chars that has "s" *
 *******************************************/
int	ft_strlen(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (0);
	while (s[i] != '\0')
		i++;
	return (i);
}
